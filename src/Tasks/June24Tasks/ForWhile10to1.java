package Tasks.June24Tasks;

public class ForWhile10to1 {
    public static void main(String[] args) {
        for (int i = 10; i >=1 ; i--) {
            System.out.println("Print Number: " + i);
        }

        int number = 10;
        while(number >= 1)
        {
            System.out.println("Enter Number - While Loop: " +number);
            number--;
        }
    }
}
