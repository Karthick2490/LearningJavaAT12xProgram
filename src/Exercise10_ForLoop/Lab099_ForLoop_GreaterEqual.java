package Exercise10_ForLoop;

public class Lab099_ForLoop_GreaterEqual {
    public static void main(String[] args) {
        for(int i = 0; i <=10; i++)
        {
            System.out.println("i = " +i);
        }
        for(int j = 1; j <=10; j++)
        {
            System.out.println("j = " +j);
        }
        for(int k = 1; k <10; k++)
        {
            System.out.println("k = " +k);
        }
    }
}
