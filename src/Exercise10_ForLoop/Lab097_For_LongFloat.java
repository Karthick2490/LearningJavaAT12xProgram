package Exercise10_ForLoop;

public class Lab097_For_LongFloat {
    public static void main(String[] args) {
        for(long i = 1l; i<=10;i++)
        {
            System.out.println(i);
        }

        for(float f = 0.0f; f<10.37;f++)
        {
            System.out.println(f);
        }
    }
}
