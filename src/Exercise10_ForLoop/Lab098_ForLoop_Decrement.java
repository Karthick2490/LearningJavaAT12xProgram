package Exercise10_ForLoop;

public class Lab098_ForLoop_Decrement {
    public static void main(String[] args) {
        for(int i = -1; i > -10; i--)
        {
            System.out.println(i);
        }

        int j = 10;
        for (; j > 0; j--) {
            System.out.println(j);
        }
    }
}
