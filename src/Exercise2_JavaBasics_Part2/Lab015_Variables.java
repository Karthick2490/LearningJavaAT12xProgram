package Exercise2_JavaBasics_Part2;

public class Lab015_Variables {
    /* datatype _variable_Name = _Variable_Value*/

    public static void main(String[] args) {
        byte age = 65;

        //byte = data type - Container
        //age = Variable name - Identifier
        //65 = Variable Value - Literal
        // = Assignment Operator

        age = 66; //New literal assigned
        System.out.println(age);
    }
}
