package Exercise13_Functions;

public class Lab125_BuiltInFunction {
    public static void main(String[] args) {
        //built-in functions in java
        //created by Java developers

        int result = Math.min(4,5);
        int result1 = Math.max(43,90);
        float result2 = Math.round(15.99);
        double result3 = Math.sqrt(4);

        System.out.println(result);
        System.out.println(result1);
        System.out.println(result2);
        System.out.println(result3);
    }
}
