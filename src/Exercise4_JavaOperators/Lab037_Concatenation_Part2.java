package Exercise4_JavaOperators;

public class Lab037_Concatenation_Part2 {
    public static void main(String[] args) {
        String s1 = "Karthick";
        String s2 = "Udayakumar";
        int a = 10;
        int b = 20;
        System.out.println(s1 + s2 + a + b);
        System.out.println( a + b + s1+ s2);
        System.out.println(s1+s2+ (a+b));
    }
}
