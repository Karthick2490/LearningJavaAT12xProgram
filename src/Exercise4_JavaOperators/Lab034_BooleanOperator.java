package Exercise4_JavaOperators;

public class Lab034_BooleanOperator {
    public static void main(String[] args) {
        int a = 5;
        int b = -5;
        boolean c = a > b;
        System.out.println(c);

        int karthick = 35;
        int bharathi = 35;
        boolean age = karthick <= bharathi;
        System.out.println(age);

        int num1 = 10;
        int num2 = 20;
        boolean result = num1 != num2;
        System.out.println(result);

        String name = "karthick";
        String expected = "Karthick";
        boolean result1 = expected == name;
        System.out.println(result1);

    }
}
